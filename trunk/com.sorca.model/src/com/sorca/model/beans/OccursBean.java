package com.sorca.model.beans;

public class OccursBean {
	/**
	 * @uml.property  name="hierarchy"
	 */
	private int hierarchy;
	/**
	 * @uml.property  name="numOccurs"
	 */
	private int numOccurs;
	/**
	 * @uml.property  name="byteCount"
	 */
	private int byteCount;
	/**
	 * @uml.property  name="index"
	 */
	private int index;
	/**
	 * @uml.property  name="lineNum"
	 */
	private int lineNum;
	
	/**
	 * @return
	 * @uml.property  name="hierarchy"
	 */
	public int getHierarchy() {
		return hierarchy;
	}
	/**
	 * @param hierarchy
	 * @uml.property  name="hierarchy"
	 */
	public void setHierarchy(int hierarchy) {
		this.hierarchy = hierarchy;
	}
	/**
	 * @return
	 * @uml.property  name="numOccurs"
	 */
	public int getNumOccurs() {
		return numOccurs;
	}
	/**
	 * @param numOccurs
	 * @uml.property  name="numOccurs"
	 */
	public void setNumOccurs(int numOccurs) {
		this.numOccurs = numOccurs;
	}
	/**
	 * @return
	 * @uml.property  name="byteCount"
	 */
	public int getByteCount() {
		return byteCount;
	}
	/**
	 * @param byteCount
	 * @uml.property  name="byteCount"
	 */
	public void setByteCount(int byteCount) {
		this.byteCount = byteCount;
	}
	/**
	 * @param index
	 * @uml.property  name="index"
	 */
	public void setIndex(int index) {
		this.index = index;
	}
	/**
	 * @return
	 * @uml.property  name="index"
	 */
	public int getIndex() {
		return index;
	}
	/**
	 * @param lineNum
	 * @uml.property  name="lineNum"
	 */
	public void setLineNum(int lineNum) {
		this.lineNum = lineNum;
	}
	/**
	 * @return
	 * @uml.property  name="lineNum"
	 */
	public int getLineNum() {
		return lineNum;
	}
	
	
	
}
